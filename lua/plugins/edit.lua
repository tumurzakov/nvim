-- Lightweight editing aids: surround (ys/ds/cs) and auto-closing pairs.
return {
  {
    "kylechui/nvim-surround",
    version = "*",
    event = "VeryLazy",
    config = function()
      require("nvim-surround").setup({})
    end,
  },
  {
    "windwp/nvim-autopairs",
    event = "InsertEnter",
    config = function()
      local npairs = require("nvim-autopairs")
      -- check_ts: don't auto-pair inside strings / comments (uses treesitter).
      npairs.setup({ check_ts = true })

      -- Auto-insert () after confirming a function/method completion in cmp.
      local ok, cmp = pcall(require, "cmp")
      if ok then
        local cmp_autopairs = require("nvim-autopairs.completion.cmp")
        cmp.event:on("confirm_done", cmp_autopairs.on_confirm_done())
      end
    end,
  },
}
